export function generateAdvice({ gender, height, currFat, currMuscle, fat, muscle }) {
   const lines = []
 
   // Рост
   if (height === 'tall') {
     lines.push('Твой рост выше среднего, поэтому тебе может быть сложнее набрать объем. Но зато если вам это удастся, весь мир будет у ваших ног. Сосредоточьтесь на базовых упражнениях (присед, становая тяга, жим). Учитывайте рычаги: при большем росте берите чуть меньший рабочий вес и чуть больше повторений — это поможет освоить технику без травм.');
   } else {
     lines.push('Твой рост ниже среднего, поэтому рост нестанет препядствием в наборе объема в мышцах, но также может способствовать набору лишнего жира. Составьте программу с упором на технику и прогрессивную нагрузку. Используйте более свободный диапазон весов: чуть больше веса при меньшем количестве повторений, чтобы держать метаболизм в тонусе и минимизировать откладывание жира.');
   }
 
   // Жир: текущий → целевой
   if (currFat !== fat) {
     if (fat === 'low') {
       lines.push('Чтобы снизить уровень жира, необходимо создать дефицит калорий. Пару дней считайте все, что вы едите, через калькулятор калорий (питаясь как обычно). Через пару дней вы увидите среднее потребление. Чтобы терять вес, снизьте этот объем на ~300–500 ккал в день, остальной дефицит обеспечьте 2–3 кардио-сессиями в неделю по 20–30 минут. Добавьте больше клетчатки и белка: овощи, цельнозерновые, нежирное мясо и бобовые позволят дольше оставаться сытым и сохранять мышечную массу.');
     } else if (fat === 'medium') {
       lines.push('Чтобы перейти к среднему уровню жира, немного уменьшите калорийность и поддерживайте 1–2 кардио-сессии в неделю. Отслеживайте прогресс не только по весу, но и по объемам тела: сантиметровая лента и фото помогают увидеть изменения, даже если вес стоит на месте.');
      }
   }
 
   // Мышцы: текущие → целевые
   if (currMuscle !== muscle) {
     if (muscle === 'medium') {
       lines.push('Для набора мышечной массы тренируйтесь 3–4 раза в неделю, делая упор на объем (8–12 повторений) и калорийный профицит (+300 ккал).Сосредоточьтесь на прогрессии: чуть больше веса или повторений на каждой тренировке — это главный ключ к росту мышц.');
     } else if (muscle === 'pro') {
       lines.push('Для значительной мышечной гипертрофии добавьте 4–5 тренировок в неделю, включите прогрессивную перегрузку и высокобелковую диету (2 г белка на кг веса). Варьируйте упражнения: подключайте изолированную работу (бицепсы, трицепсы) и технику медленных повторений, чтобы проработать каждую мышечную группу максимально глубоко.');
     }
   }
 
   // Основы питания
   lines.push('Следите за макронутриентами: примерно 40% углеводов, 30% белка и 30% жиров, но ориентируйтесь на собственные ощущения и прогресс. Пейте достаточно воды — около 30–40 мл на кг массы тела в день. Гидратация важна для работы мышц, суставов и общего метаболизма.');
 
   // Учёт прогресса
   lines.push('Ведите дневник тренировок и питания: записывайте веса, повторения, кардио, замеры тела и самочувствие. Это поможет скорректировать план в нужный момент. Делайте фото до и после раз в месяц — визуальное сравнение часто даёт больше мотивации, чем цифры на весах.');
 
   // Восстановление и движение
   lines.push('Восстановление не менее важно, чем тренировки. Дайте себе 1–2 дня отдыха в неделю и слушайте свое тело: если чувствуете сильную усталость, замените тяжелую тренировку на легкий фитнес или прогулку. Не пренебрегайте разминкой и растяжкой: 5–10 минут легкого кардио плюс суставная гимнастика перед нагрузкой помогут снизить риск травм.');
 
   // Добавки (по желанию)
   lines.push('Если хотите, можно добавить базовые добавки: креатин, сывороточный протеин, омега-3 — но сперва наладьте рацион из обычных продуктов.');
 
   // Ментальный подход
   lines.push('Будьте терпеливы и последовательны. Прогресс иногда идет маленькими шагами, но со временем эти шаги складываются в большой результат. Фокусируйтесь на ощущениях мышц во время упражнения (mind–muscle connection), это поможет качественнее прорабатывать целевые группы.');
 
   // Программы и вариации
   lines.push('Если не знаешь как составить тренировку, или программу, используй любые готовые из интернета, главное выбирать так чтобы ты мог выполнить ее в правильной технике. Если выберешь программу — следуй ей минимум месяц, меняй только если она стала слишком легкой для тебя в процессе. Можешь комбинировать несколько программ например совмещать бег и подтягивания (главное чтобы рабочие мышцы не пересекались). Если чувствуешь, что программа стала рутинной, пробуй менять порядок упражнений или добавлять супершеты, чтобы поддерживать интерес и повышать интенсивность тренировок.');
 
   // Финальное ободрение
   lines.push('Не забывайте про главное: регулярность, правильная техника и позитивный настрой. Я верю в тебя, дружище)');
 
   return lines;
 }
 