// Правила для базовых советов
export function generateAdvice({ gender, height, currFat, currMuscle, fat, muscle }) {
   const lines = []
 
   // Рост
   if (height === 'tall') {
     lines.push('У вас высокий рост — может быть сложнее набрать объем. Сосредоточьтесь на базовых упражнениях (присед, становая тяга, жим).')
   } else if (height === 'short') {
     lines.push('Низкий рост дает механическое преимущество при многих упражнениях — используйте это, чтобы прогрессировать в весах.')
   } else {
     lines.push('Средний рост — хороший баланс. Составьте программу с упором на технику и прогрессивную нагрузку.')
   }
 
   // Жир: текущий → целевой
   if (currFat !== fat) {
     if (fat === 'low') {
       lines.push('Чтобы снизить уровень жира, создайте дефицит калорий (~300-500 ккал в день) и добавьте 2-3 кардио-сессии в неделю по 20–30 мин.')
     } else if (fat === 'medium') {
       lines.push('Чтобы перейти к среднему уровню жира, немного уменьшите калорийность и поддерживайте 1-2 кардио-сессии в неделю.')
     }
   }
 
   // Мышцы: текущие → целевые
   if (currMuscle !== muscle) {
     if (muscle === 'medium') {
       lines.push('Для набора мышечной массы тренируйтесь 3–4 раза в неделю, делая упор на объем (8–12 повторений) и калорийный профицит (+300 ккал).')
     } else if (muscle === 'pro') {
       lines.push('Для значительной мышечной гипертрофии добавьте 4–5 тренировок в неделю, включите прогрессивную перегрузку и высокобелковую диету (2 г/кг веса).')
     }
   }
 
   // Общие советы
   lines.push('Не забывайте о полноценном восстановлении: сон 7–8 часов и растяжка после тренировок.')
   lines.push('Следите за техникой, прогрессируйте нагрузку постепенно и ведите дневник тренировок.')
 
   return lines
 } 